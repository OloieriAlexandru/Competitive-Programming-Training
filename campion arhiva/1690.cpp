void sett(int x, int y, char s[7][4]){
    for (int i=0;i<7;++i){
        for (int j=0;j<4;++j){
            if (s[i][j] && x+i<=n && y+j <= m){
                st[x+i].set(y+j);
            }
        }
    }
}
